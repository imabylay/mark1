# If-else statements

# 1
a = 200
b = 33

if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")

# 2
number = 7
if number % 2 == 0:
    print("The number is even")
else:
    print("The number is odd")

# 3
username = ""
if username:
    print("Welcome,", username)
else:
    print("Username is empty")

# 4
score = 85
if score >= 60:
    print("Passed")
else:
    print("Failed")

# 5
is_active = False
if is_active:
    print("Account is active")
else:
    print("Account is inactive")
