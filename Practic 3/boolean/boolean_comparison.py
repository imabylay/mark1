# Python comparison operators

x = 5
y = 3

# 1: Equal and not equal
print(x == y)
print(x != y)

print("-" * 30)

# 2: Greater than and less than
print(x > y)
print(x < y)

print("-" * 30)

# 3: Greater than or equal / less than or equal
print(x >= y)
print(x <= y)

print("-" * 30)

# 4: Comparison with different values
a = 10
b = 10

print(a == b)
print(a > b)

print("-" * 30)

# 5: Chained comparisons
z = 7

print(1 < z < 10)
print(5 < z < 6)
